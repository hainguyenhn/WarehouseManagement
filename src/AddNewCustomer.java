    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            String userName = "'"+ usernameTextField.getText()+"'";
            String password = "'"+passwordField.getText()+"'";
            String firstName = "'"+firstnameTextField.getText()+"'";
            String lastName = "'"+lastnameTextField.getText()+"'";
            String phoneNumber = "'"+phoneTextField.getText()+"'";
            String address = "'"+addressTextField.getText()+"'";
            String role = "'"+"Customer"+"'";
            if(jRadioButton1.isSelected())
                role = "'"+"Admin"+"'";
            
            ArrayList customerInfor = new ArrayList();
            customerInfor.add(userName);
            customerInfor.add(password);
            customerInfor.add(firstName);
            customerInfor.add(lastName);
            customerInfor.add(phoneNumber);
            customerInfor.add(address);
            customerInfor.add(role);
            
            boolean success = Database.addCustomer(customerInfor);
            if (success)
            {
                JOptionPane.showMessageDialog(null,"Insert Complete");
                this.setVisible(false);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Insert Failed");
                new AddNewCustomer();
            }
    }//GEN-LAST:event_jButton1ActionPerformed

