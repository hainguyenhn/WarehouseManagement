    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       int myId = Database.getCustomerId(customerId.getText());
       Customer result = Database.searchCustomer(myId,new Customer());
       
       if(result == null)
       {
           jLabel6.setVisible(true);
       }
       else
       {
           jLabel6.setVisible(false);
           usernameTextField1.setText(result.userName);
           passwordTextField1.setText(result.password);
           firstnameTextField1.setText(result.firstName);
           lastnameTextField1.setText(result.lastName);
           phoneTextField1.setText(result.phone);
           addressTextField1.setText(result.address);
           if("Admin".equals(result.role))
            {
               adminRadio.setSelected(true);
               customerRadio.setSelected(false);
            }   
           else
            {
                customerRadio.setSelected(true);
                adminRadio.setSelected(false);
            }
       }
       
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        boolean success = false;
        
      Customer updatedCustomer = new Customer();
      updatedCustomer.customerId = Integer.valueOf(customerId.getText());
      updatedCustomer.userName = usernameTextField1.getText();
      updatedCustomer.password = passwordTextField1.getText();
      updatedCustomer.firstName = firstnameTextField1.getText();
      updatedCustomer.lastName = lastnameTextField1.getText();
      updatedCustomer.phone = phoneTextField1.getText();
      updatedCustomer.address = addressTextField1.getText();
      if(adminRadio.isSelected())
        {
          updatedCustomer.role = "Admin";
          customerRadio.setSelected(false);
        }
      else
         {
             updatedCustomer.role = "Customer";
         }
      
      success = Database.updateCustomer(updatedCustomer.customerId,updatedCustomer);
      if(success)
        {
          jLabel6.setText("Customer Updated Completed");
          jLabel6.setForeground(Color.blue);
          jLabel6.setVisible(true);
        }
      else
      {
          jLabel6.setText("Customer Updated Failed");
          jLabel6.setForeground(Color.red);
          jLabel6.setVisible(true);
      }
    }//GEN-LAST:event_jButton4ActionPerformed

